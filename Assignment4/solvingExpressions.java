package Day1.Assignment4;

public class solvingExpressions {

	public static void main(String[] args) {
		int x = 5;
		int y = 10;
		System.out.println("x + y * 2 = " + (x + y * 2));
		System.out.println("x - y + 2 = " + (x - y + 2));
		System.out.println("(x+y) * 2 = " + (x + y) * 2);
		System.out.println("y % x = " + y % x);

	}

}
